#!/usr/bin/env python3
"""Generate a deterministic markdown index of project markdown references.

Sources:
- .claude/plans
- .tickets
- docs
"""

from __future__ import annotations

import argparse
import re
from dataclasses import dataclass
from datetime import datetime, timezone
from pathlib import Path
from typing import Iterable


DEFAULT_SOURCE_DIRS = [
    ".claude/plans",
    ".tickets",
    "docs",
]


@dataclass(frozen=True)
class MdRecord:
    path: Path
    last_modified: datetime
    title: str


def _iter_md_files(source_dir: Path) -> Iterable[Path]:
    if not source_dir.exists():
        return []

    files = list(source_dir.rglob("*.md"))
    return sorted(files, key=lambda p: str(p))


def _extract_title(path: Path) -> str:
    heading_re = re.compile(r"^\s{0,3}#{1,6}\s+(.*?)\s*$")
    in_front_matter = False
    is_first_line = True

    try:
        with path.open("r", encoding="utf-8") as handle:
            for line in handle:
                stripped = line.strip("\n")

                if is_first_line:
                    is_first_line = False
                    if stripped == "---":
                        in_front_matter = True

                if in_front_matter:
                    if stripped == "---":
                        in_front_matter = False
                    continue

                match = heading_re.match(line)
                if match:
                    text = match.group(1).strip()
                    return text or path.stem.replace("-", " ")

                if stripped:
                    # keep scanning until first heading line appears
                    continue
    except (OSError, UnicodeDecodeError):
        return path.stem.replace("-", " ")

    return path.stem.replace("-", " ")


def _escape_table_value(value: str) -> str:
    return value.replace("|", "\\|").replace("\n", " ")


def build_records(
    repo_root: Path, source_dirs: list[str], output_path: Path
) -> list[MdRecord]:
    records: list[MdRecord] = []

    for source in source_dirs:
        source_dir = repo_root / source
        for file_path in _iter_md_files(source_dir):
            if file_path.resolve() == output_path:
                continue
            stat = file_path.stat()
            mtime = datetime.fromtimestamp(stat.st_mtime, tz=timezone.utc)
            title = _extract_title(file_path)
            records.append(
                MdRecord(
                    path=file_path.relative_to(repo_root),
                    last_modified=mtime,
                    title=title,
                )
            )

    records.sort(key=lambda record: str(record.path).lower())
    return records


def render_markdown(records: list[MdRecord], source_dirs: list[str]) -> str:
    lines = [
        "# Pi Extension Markdown Reference Index",
        "",
        "Generated by `bin/refresh_md_index.py` (local-only, reproducible),",
        "sorted for deterministic output.",
        "",
        "## Sources",
    ]

    for source in source_dirs:
        lines.append(f"- `{source}`")

    lines.extend(
        [
            "",
            "## References",
            "",
            "| Path | Last Modified (UTC) | Title |",
            "| --- | --- | --- |",
        ]
    )

    for row in records:
        path = row.path.as_posix()
        date = row.last_modified.strftime("%Y-%m-%d %H:%M:%S UTC")
        title = _escape_table_value(row.title)
        lines.append(f"| `{path}` | {date} | {title} |")

    if not records:
        lines.append(
            "| *(No markdown references found in configured source directories yet)* |"
            " *(none)* | *(none)* |"
        )

    lines.extend(
        [
            "",
            f"Total documents: {len(records)}",
        ]
    )

    return "\n".join(lines) + "\n"


def parse_args(argv: list[str] | None = None) -> argparse.Namespace:
    parser = argparse.ArgumentParser()
    parser.add_argument(
        "--repo-root",
        default=str(Path.cwd()),
        help="Repository root path (default: current working directory)",
    )
    parser.add_argument(
        "--output",
        default="docs/PI_EXTENSION_MD_INDEX.md",
        help="Output index markdown path",
    )
    parser.add_argument(
        "--sources",
        nargs="*",
        default=DEFAULT_SOURCE_DIRS,
        help="Source directories relative to repo-root",
    )
    return parser.parse_args(argv)


def main() -> int:
    args = parse_args()
    repo_root = Path(args.repo_root).resolve()
    output = (repo_root / args.output).resolve()
    source_dirs = args.sources

    records = build_records(repo_root, source_dirs, output)
    output.parent.mkdir(parents=True, exist_ok=True)
    output.write_text(
        render_markdown(records, source_dirs),
        encoding="utf-8",
    )
    print(f"Wrote {len(records)} entries to {output.relative_to(repo_root)}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
